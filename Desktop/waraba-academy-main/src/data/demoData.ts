// Fonction utilitaire pour récupérer un cours par ID
export const getCourseById = (id: string): Course | undefined => {
  return demoCourses.find(course => course.id === id || course.slug === id);
};

// Fonction utilitaire pour récupérer les cours par catégorie
export const getCoursesByCategory = (categorySlug: string): Course[] => {
  return demoCourses.filter(course => course.category.slug === categorySlug);
};

// Fonction utilitaire pour récupérer les cours populaires
export const getPopularCourses = (limit: number = 6): Course[] => {
  return demoCourses
    .filter(course => course.isPopular)
    .sort((a, b) => b.totalStudents - a.totalStudents)
    .slice(0, limit);
};

// Fonction utilitaire pour récupérer les cours recommandés
export const getRecommendedCourses = (_userId: string, limit: number = 6): Course[] => {
  // Logique simple de recommandation basée sur la popularité
  return demoCourses
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};
